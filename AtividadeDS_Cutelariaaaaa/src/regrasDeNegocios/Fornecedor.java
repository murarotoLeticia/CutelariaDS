
package regrasDeNegocios;

public class Fornecedor {
    private int id_fornecedor;
    private String nome_for;
    private String cnpj_for;
    private String email_for;
    private String telefone_for;
    private String material_fornecido;
    private String cidade_for;
    private String rua_for;
    private String bairro_for;
    private String numero_lugar_for;

    public int getId_fornecedor() {
        return id_fornecedor;
    }

    public void setId_fornecedor(int id_fornecedor) {
        this.id_fornecedor = id_fornecedor;
    }

    public String getNome_for() {
        return nome_for;
    }

    public void setNome_for(String nome_for) {
        this.nome_for = nome_for;
    }

    public String getCnpj_for() {
        return cnpj_for;
    }

    public void setCnpj_for(String cnpj_for) {
        this.cnpj_for = cnpj_for;
    }

    public String getEmail_for() {
        return email_for;
    }

    public void setEmail_for(String email_for) {
        this.email_for = email_for;
    }

    public String getTelefone_for() {
        return telefone_for;
    }

    public void setTelefone_for(String telefone_for) {
        this.telefone_for = telefone_for;
    }

    public String getMaterial_fornecido() {
        return material_fornecido;
    }

    public void setMaterial_fornecido(String material_fornecido) {
        this.material_fornecido = material_fornecido;
    }

    public String getCidade_for() {
        return cidade_for;
    }

    public void setCidade_for(String cidade_for) {
        this.cidade_for = cidade_for;
    }

    public String getRua_for() {
        return rua_for;
    }

    public void setRua_for(String rua_for) {
        this.rua_for = rua_for;
    }

    public String getBairro_for() {
        return bairro_for;
    }

    public void setBairro_for(String bairro_for) {
        this.bairro_for = bairro_for;
    }

    public String getNumero_lugar_for() {
        return numero_lugar_for;
    }

    public void setNumero_lugar_for(String numero_lugar_for) {
        this.numero_lugar_for = numero_lugar_for;
    }
    
    

   
 
}
