    public void preencherTabela(){
    ProdutoDao pdao = new ProdutoDao(); 
    List<Produto> lista= pdao.listarTodos();
    DefaultTableModel tabela =  (DefaultTableModel) jT_produto.getModel();
    tabela.setRowCount(0);
    for(Produto p: lista){
        tabela.addRow(new Object[]{p.getId_produto(), p.getNome_pro(), p.getQuantidade_pro(), p.getModelo_pro(), p.getTipo_pro(), p.getValor_pro()});
    }
    
    }
