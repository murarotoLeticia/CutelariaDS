    private void jB_editarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_editarActionPerformed
        // TODO add your handling code here:
        int opcao = jT_produto.getSelectedRow(); //Pega a lh selecionada que esta na tabela
        if(opcao>=0){
            edID.setText(jT_produto.getValueAt(opcao,0).toString());
            edNome.setText(jT_produto.getValueAt(opcao, 1).toString()); // atribuindo um valor aos campos
            edQuantidade.setText(jT_produto.getValueAt(opcao, 2).toString());
            edModelo.setText(jT_produto.getValueAt(opcao, 3).toString());
            edTipoProduto.setText(jT_produto.getValueAt(opcao, 4).toString());
            edValor.setText(jT_produto.getValueAt(opcao, 5).toString());
            jb_atualizar.setVisible(true);
            jb_Salvar.setVisible(false);
            jTabbedPane1.setSelectedIndex(0);


        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha");
        }

    }//GEN-LAST:event_jB_editarActionPerformed

    private void jB_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_excluirActionPerformed
        // TODO add your handling code here:
        int opcao = jT_produto.getSelectedRow(); //Pega a lh selecionada que esta na tabela
        if(opcao>=0){
            Produto p = new Produto();
            p.setId_produto(Integer.parseInt(jT_produto.getValueAt(opcao,0).toString()));
            p.setNome_pro(jT_produto.getValueAt(opcao, 1).toString());
            p.setQuantidade_pro(jT_produto.getValueAt(opcao, 2).toString());
            p.setModelo_pro(jT_produto.getValueAt(opcao, 3).toString());
            p.setTipo_pro(jT_produto.getValueAt(opcao, 4).toString());
            p.setValor_pro(Float.parseFloat (jT_produto.getValueAt(opcao, 5).toString()));

            ProdutoDao pdao = new ProdutoDao();
            pdao.deletar(p);
            preencherTabela(); // para atualizar tabela
        }else{
            JOptionPane.showMessageDialog(null, "Selecione uma linha");

}


    }//GEN-LAST:event_jB_excluirActionPerformed

